<?php
# ข้อมูลเป็น array2มิติ
$a = [ [16,12], 
    [29,18], 
    [17,21], 
    [9,10], 
    [10,15 ]];

    #ในส่วนของ for loop คือ กำหนดจำนวนให้ค่ารันตามที่กำหนด 
for ($i = 0; $i < count($a); $i++) { 
    #คำสั่ง วนลูป คำสั่งนี้ count($a)คือ การรันค่าทุกตัว ถ้าอยากกำหนดค่าให้รันตามจำนวนที่กำหนดก็ลบ count($a)และใส่ตัวเลขแทน
    echo "<br>";
    echo "ผลรวม : "; 
    for ($j = 0; $j < count($a[$i]); $j++) {
        echo  $a[$i][$j] , " ";
    }
}
?>